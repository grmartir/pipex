#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	i;

	i = c;
	if ((i >= 48 && i <= 57) || (i >= 65 && i <= 90) || (i >= 97 && i <= 122))
		return (1);
	return (0);
}
